-- คำนวณส่วนลดโปรโมชั่นทั้งหมดที่ใช้ได้กับบิลนี้ ณ ตอนนี้ (เรียกใหม่ทุกครั้งที่รายการเปลี่ยน)
-- นับจำนวนเฉพาะสินค้าที่เลือกไว้ให้โปรโมชั่นนั้นๆ เท่านั้น (ไม่ใช่ทั้งบิล) ลดตายตัวตาม discount_amount
create or replace function public.calculate_promo_discount(p_sale_id uuid)
returns numeric
language plpgsql
security definer
set search_path = public
as $$
declare
  v_discount numeric := 0;
  v_promo record;
  v_qty numeric;
begin
  for v_promo in
    select * from promotions
    where is_active = true
      and type = 'buy_x_get_fixed_discount'
      and coalesce(threshold_qty, 0) > 0
      and coalesce(discount_amount, 0) > 0
  loop
    select coalesce(sum(si.quantity), 0)
      into v_qty
      from sale_items si
      where si.sale_id = p_sale_id
        and si.product_id in (
          select pp.product_id from promotion_products pp where pp.promotion_id = v_promo.id
        );

    if v_qty > 0 then
      v_discount := v_discount + floor(v_qty / v_promo.threshold_qty) * v_promo.discount_amount;
    end if;
  end loop;

  return v_discount;
end;
$$;

revoke execute on function public.calculate_promo_discount from public, anon;
grant execute on function public.calculate_promo_discount to authenticated;

-- คำนวณส่วนลดโปรใหม่ให้ทุกบิลที่ยังเปิดอยู่ (โต๊ะที่กำลังสั่งอยู่) ทันทีที่โปรโมชั่นเปลี่ยน
-- (เพิ่ม/ลบเมนูในโปร, เปิด/ปิดโปร, แก้เงื่อนไข) กันปัญหาบิลที่สั่งไปแล้วไม่ได้ส่วนลดจนกว่าจะสั่งเพิ่ม
create or replace function public.recalculate_open_sales_promo()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_sale record;
  v_discount numeric;
begin
  for v_sale in select id, subtotal, discount from sales where status = 'open' loop
    v_discount := public.calculate_promo_discount(v_sale.id);
    if v_discount is distinct from v_sale.discount then
      update sales set discount = v_discount, total = greatest(v_sale.subtotal - v_discount, 0)
        where id = v_sale.id;
    end if;
  end loop;
  return null;
end;
$$;

