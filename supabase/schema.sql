-- หมวดหมู่สินค้า
create table if not exists public.categories (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  position int not null default 0,
  created_at timestamptz not null default now()
);

-- สินค้า
create table if not exists public.products (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  barcode text,
  category_id uuid references public.categories (id) on delete set null,
  price numeric(12, 2) not null default 0,        -- ราคาขาย
  cost numeric(12, 2) not null default 0,         -- ต้นทุนต่อหน่วย
  stock numeric(12, 2) not null default 0,        -- จำนวนคงเหลือ
  track_stock boolean not null default true,      -- ตัดสต๊อกเมื่อขายหรือไม่
  low_stock_threshold numeric(12, 2) not null default 5,
  image_url text,
  is_active boolean not null default true,
  is_sold_out boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_products_category on public.products (category_id);
create index if not exists idx_products_barcode on public.products (barcode);

-- บิลขาย
create table if not exists public.sales (
  id uuid primary key default gen_random_uuid(),
  sale_number bigint generated by default as identity,
  subtotal numeric(12, 2) not null default 0,
  discount numeric(12, 2) not null default 0,
  total numeric(12, 2) not null default 0,
  cost_total numeric(12, 2) not null default 0,   -- ต้นทุนรวมของบิล (บันทึก ณ เวลาขาย)
  payment_method text not null default 'cash',    -- cash | transfer | card
  received numeric(12, 2),                        -- เงินที่รับมา (เงินสด)
  change numeric(12, 2),                          -- เงินทอน
  note text,
  status text not null default 'completed',       -- open | completed | voided
  voided_at timestamptz,
  user_id uuid references auth.users (id),
  created_at timestamptz not null default now()
);

create index if not exists idx_sales_created_at on public.sales (created_at);
create index if not exists idx_sales_status on public.sales (status);

-- รายการสินค้าในบิล (บันทึกราคา/ต้นทุน ณ เวลาขาย)
